    // MMCM. On hardware the MMCM locks within milliseconds of FPGA
    // configuration -- long before UHD ever talks to the block. In simulation
    // the lock takes a non-trivial amount of modeled time, and the data-path
    // flush in flush_and_reset spans the ce_250 domain, so it cannot complete
    // until the MMCM has locked. Wait for lock here (mirroring the hardware
    // power-up ordering) before exercising any flush/reset.
    // Level-sensitive wait (no fork/disable, so the BFM processes started by
    // run() are untouched). If the clock never comes up, the global start_tb
    // timeout catches it.
    $display("Waiting for ce_250 MMCM to lock...");
    wait (dut.ce_250_locked === 1'b1);
    $display("ce_250 MMCM locked at t=%0t", $time);

    // Coordinated reset after the ce_250 clock is up. The block's chdr<->ce_250
    // CDC FIFOs (in the NoC shell) must be reset on both sides with both clocks
    // running. At power-up the chdr side reset before ce_250_clk existed (it
    // only starts once the MMCM locks), so issue a chdr-path reset here to
    // re-initialize those FIFOs cleanly. On hardware the MMCM is locked long
    // before UHD ever talks to the block, so this ordering is automatic.
    blk_ctrl.reset_chdr();

