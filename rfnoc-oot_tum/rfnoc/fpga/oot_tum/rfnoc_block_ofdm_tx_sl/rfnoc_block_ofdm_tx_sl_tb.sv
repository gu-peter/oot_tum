  localparam [31:0] NOC_ID          = 32'hFF2494FF;
  localparam [ 9:0] THIS_PORTID     = 10'h123;
  localparam int    CHDR_W          = 64;    // CHDR size in bits
  localparam int    MTU             = 10;    // Log2 of max transmission unit in CHDR words
  localparam int    NUM_PORTS_I     = 1;
  localparam int    NUM_PORTS_O     = 1;
  localparam int    ITEM_W          = 32;    // Sample size in bits
  localparam int    SPP             = 64;    // Samples per packet
  localparam int    PKT_SIZE_BYTES  = SPP * (ITEM_W/8);
  localparam int    STALL_PROB      = 25;    // Default BFM stall probability
  // Clock periods match the X440 X4_200 hardware image:
  //   rfnoc_chdr_clk : CHDR_CLK_RATE = 200 MHz       (x4xx.sv)
  //   rfnoc_ctrl_clk : clk40         = 40 MHz        (x4xx.sv)
  //   ce_clk         : clk200*4/3    = 266.667 MHz   (x4xx.sv ce_clk_gen, fixed,
  //                    independent of the 250 MHz master clock rate). The block
  //                    derives a 250 MHz clock (ce_250) from ce_clk with an
  //                    internal MMCM and runs the OFDM core + data path in that
  //                    250 MHz domain, so ce_clk only feeds the MMCM here.
  localparam real   CHDR_CLK_PER    = 5.0;    // 200 MHz
  localparam real   CTRL_CLK_PER    = 25.0;   // 40 MHz
  localparam real   CE_CLK_PER      = 3.75;   // 266.667 MHz

  // Client register map (matches rfnoc_block_ofdm_tx_sl.sv). These are raw
  // CtrlPort addresses; the BFM passes the address straight through to
  // m_ctrlport_req_addr, which the block decodes against these literals.
  localparam int    REG_TX_PAYLOAD_READY_ADDR = 0;  // RO: txPayload input ready
  localparam int    REG_ENABLE_ADDR           = 1;  // RW: OFDM transmitter enable

  // Stimulus configuration
  //
  // The txPayload input port carries one int8 sample per byte, but only the
  // two LSBs are used by the block (bit[0] -> txPayload_0, bit[1] -> txPayload_1,
  // i.e. the QPSK I/Q bits). We feed NUM_TX_SAMPS samples, gating on the
  // txPayloadReady status register, then assert "enable" so the block starts
  // emitting modulated sc16 samples on its output port.
  localparam int    NUM_TX_SAMPS    = 40812;  // int8 payload samples to feed
  localparam int    NUM_RX_SAMPS    = 76800;  // sc16 output samples to capture

  // Golden-reference comparison. The expected sc16 output and the matching
  // payload stimulus are generated by the MATLAB reference (runIntroOFDMML.m,
  // variable txWaveformML) and exported as hex (one value per line):
  //   - INPUT_FILE:    one payload sample per line, "%02X" (bit0->txPayload_0,
  //                    bit1->txPayload_1, i.e. the QPSK I/Q bits).
  //   - EXPECTED_FILE: one sc16 output word per line, "%08X" = {I[31:16],Q[15:0]},
  //                    Q1.15 fixed point (1.0 -> 32768).
  // The MATLAB and FPGA datapaths are not bit-exact, so each captured I/Q value
  // is allowed to deviate by TOL_LSB. The spec is +-0.001 in the normalized
  // double domain, which is 0.001*32768 ~= 32.77 LSBs in Q1.15 -> 33.
  // xsim's run directory is deep inside the Vivado sim project, so reference the
  // vectors (which live next to this testbench) by absolute path.
  localparam string VEC_DIR =
    "/home/peter/git/oot_tum/rfnoc-oot_tum/rfnoc/fpga/oot_tum/rfnoc_block_ofdm_tx_sl/";
  localparam string INPUT_FILE      = {VEC_DIR, "ofdm_tx_input.hex"};
  localparam string EXPECTED_FILE   = {VEC_DIR, "ofdm_tx_expected.hex"};
  localparam int    TOL_LSB         = 33;

  // Spectral-windowing overlap length, in output samples. The OFDM modulator
  // tapers each symbol with a raised-cosine window and overlap-adds the
  // ramp-down tail of the previous symbol onto the ramp-up head of the next
  // (WindowingSysObj, 9-deep overlap buffers). Consequently the first
  // WINDOW_OVERLAP samples of any frame whose predecessor was not flushed carry
  // a (ramp_down * previous-frame tail) term. This is mathematically correct
  // continuous windowed-OFDM behavior; the standalone MATLAB golden assumes a
  // zero history, which only the first post-reset frame satisfies. For a frame
  // that follows another (e.g. frame 2 across a payload gap) we therefore skip
  // these leading overlap samples in the golden comparison.
  localparam int    WINDOW_OVERLAP  = 9;

  // Memories loaded from the MATLAB reference files.
  logic [ 7:0] input_mem    [NUM_TX_SAMPS];
  logic [31:0] expected_mem [NUM_RX_SAMPS];

  //---------------------------------------------------------------------------
  // Clocks and Resets
  //---------------------------------------------------------------------------

  bit rfnoc_chdr_clk;
  bit rfnoc_ctrl_clk;
  bit ce_clk;

  sim_clock_gen #(CHDR_CLK_PER) rfnoc_chdr_clk_gen (.clk(rfnoc_chdr_clk), .rst());
  sim_clock_gen #(CTRL_CLK_PER) rfnoc_ctrl_clk_gen (.clk(rfnoc_ctrl_clk), .rst());
  sim_clock_gen #(CE_CLK_PER) ce_clk_gen (.clk(ce_clk), .rst());

  //---------------------------------------------------------------------------
  // Continuous Radio Sink Model (hardware-faithful capture path)
  //---------------------------------------------------------------------------
  //
  // The default capture path (recv_items via the slave BFM) reads only on
  // tvalid && tready, so it *compacts* the stream and can never see a starved
  // radio. On hardware (transceiver.py) the block output is transmitted by the
  // radio's TX, which runs at a *fixed* 250 MHz and consumes one sample every
  // cycle whether or not the block has produced one. When the radio's TX FIFO
  // empties it underflows and transmits stale/garbage (not the block's data),
  // which is exactly the corruption transceiver.py observes.
  //
  // This model reproduces that sink:
  //   * RADIO_CLK runs at 250 MHz (the radio sample clock; note ce_clk is
  //     266.667 MHz, so the radio is the slower, rate-setting consumer).
  //   * radio_fifo models the radio TX buffer. The feeder pushes block output
  //     into it and *blocks when it is full*, so the radio rate backpressures
  //     the block exactly like CHDR flow control does on hardware.
  //   * Every radio cycle the radio pops one sample; on empty it records an
  //     underflow (a non-zero garbage marker, as hardware does) instead.
  //
  // A correctly-rate-matched block keeps the FIFO non-empty after priming, so
  // the radio sees zero mid-stream underflows and captures golden in order. A
  // block that cannot sustain 250 MHz starves the radio and the capture is
  // corrupted -- the hardware failure.
  localparam real RADIO_CLK_PER       = 4.0;     // 250 MHz radio sample clock
  localparam int  RADIO_TX_FIFO_DEPTH = 8192;    // modeled radio TX buffer depth
  localparam logic [31:0] RADIO_UNDERFLOW_FILL = 32'hDEAD_DEAD;  // non-zero, like HW

  bit radio_clk;
  sim_clock_gen #(RADIO_CLK_PER) radio_clk_gen (.clk(radio_clk), .rst());

  logic [31:0] radio_fifo [$];   // radio TX FIFO (block writes, radio drains)
  logic [31:0] radio_cap  [$];   // what the radio transmits each cycle (with fills)
  bit          radio_run = 1'b0; // start draining once the FIFO has primed
  int          radio_underflows_mid = 0;  // underflow cycles after priming
  logic [31:0] radio_last = 32'b0;

  // Radio TX: one sample per 250 MHz cycle, underflow-fill on empty.
  always @(posedge radio_clk) begin
    if (radio_run) begin
      if (radio_fifo.size() > 0) begin
        radio_last = radio_fifo.pop_front();
        radio_cap.push_back(radio_last);
      end else begin
        radio_cap.push_back(RADIO_UNDERFLOW_FILL);
        radio_underflows_mid++;
      end
    end
  end

  //---------------------------------------------------------------------------
  // OFDM demodulator (subcarrier-integrity self-check)
  //---------------------------------------------------------------------------
  //
  // The golden-reference tests prove the datapath against a *fixed* MATLAB
  // payload. Constant or period-4 payloads are immune to dropped / duplicated /
  // mis-timed payload reads (every byte is identical, or repeats every 4), so
  // they can pass even when the payload->subcarrier delivery is broken -- which
  // is exactly the failure seen on hardware (data subcarriers carry the correct
  // power on the correct bins but *random* bits). This demodulator lets a test
  // feed an *arbitrary* payload and verify, per subcarrier, that the modulated
  // QPSK bits equal the bits that were fed -- no golden file required.
  //
  // Recovered convention (reverse-engineered from the golden input/output pair
  // and validated to decode golden -> input at 100%): drop the 64-sample CP,
  // take the 256-pt DFT, read the 228 data bins in fft-shifted order (negative
  // freqs 142..255, then positive freqs 0..113); per bin b0 = (Re < 0), b1 =
  // (Im < 0), payload byte = {b1, b0}. I is the upper sc16 half, Q the lower.
  localparam int  FFT_LEN_C   = 256;
  localparam int  CP_LEN_C    = 64;
  localparam int  SPS_C       = FFT_LEN_C + CP_LEN_C;  // 320
  localparam int  N_DATA_BINS = 228;
  localparam real TWO_PI      = 6.28318530717958647692;
  // How many data symbols the demod check covers. A payload-delivery bug shows
  // in the first data symbols (and accumulates), so a prefix suffices and keeps
  // the DFT cost down; raise toward 179 (all data symbols/frame) for an
  // exhaustive check.
  localparam int  DEMOD_SYMS_TO_CHECK = 40;

  real demod_cos [FFT_LEN_C];
  real demod_sin [FFT_LEN_C];
  int  demod_bins [N_DATA_BINS];
  logic [31:0] demod_cap [$];   // captured sc16 output for the demod check

  initial begin : init_demod_tables
    int idx;
    for (int m = 0; m < FFT_LEN_C; m++) begin
      demod_cos[m] = $cos(TWO_PI * m / FFT_LEN_C);
      demod_sin[m] = $sin(TWO_PI * m / FFT_LEN_C);
    end
    idx = 0;
    for (int k = 142; k < 256; k++) demod_bins[idx++] = k;  // negative freqs
    for (int k = 0;   k < 114; k++) demod_bins[idx++] = k;  // positive freqs
  end

  //---------------------------------------------------------------------------
  // Bus Functional Models
  //---------------------------------------------------------------------------

  // Backend Interface
  RfnocBackendIf backend (rfnoc_chdr_clk, rfnoc_ctrl_clk);

  // AXIS-Ctrl Interface
  AxiStreamIf #(32) m_ctrl (rfnoc_ctrl_clk, 1'b0);
  AxiStreamIf #(32) s_ctrl (rfnoc_ctrl_clk, 1'b0);

  // AXIS-CHDR Interfaces
  AxiStreamIf #(CHDR_W) m_chdr [NUM_PORTS_I] (rfnoc_chdr_clk, 1'b0);
  AxiStreamIf #(CHDR_W) s_chdr [NUM_PORTS_O] (rfnoc_chdr_clk, 1'b0);

  // Block Controller BFM
  RfnocBlockCtrlBfm #(CHDR_W, ITEM_W) blk_ctrl = new(backend, m_ctrl, s_ctrl);

  // CHDR word and item/sample data types
  typedef ChdrData #(CHDR_W, ITEM_W)::chdr_word_t chdr_word_t;
  typedef ChdrData #(CHDR_W, ITEM_W)::item_t      item_t;

  // Connect block controller to BFMs
  for (genvar i = 0; i < NUM_PORTS_I; i++) begin : gen_bfm_input_connections
    initial begin
      blk_ctrl.connect_master_data_port(i, m_chdr[i], PKT_SIZE_BYTES);
      blk_ctrl.set_master_stall_prob(i, STALL_PROB);
    end
  end
  for (genvar i = 0; i < NUM_PORTS_O; i++) begin : gen_bfm_output_connections
    initial begin
      blk_ctrl.connect_slave_data_port(i, s_chdr[i]);
      blk_ctrl.set_slave_stall_prob(i, STALL_PROB);
    end
  end

