    .s_rfnoc_chdr_tvalid (s_rfnoc_chdr_tvalid),
    .s_rfnoc_chdr_tready (s_rfnoc_chdr_tready),
    .m_rfnoc_chdr_tdata  (m_rfnoc_chdr_tdata),
    .m_rfnoc_chdr_tlast  (m_rfnoc_chdr_tlast),
    .m_rfnoc_chdr_tvalid (m_rfnoc_chdr_tvalid),
    .m_rfnoc_chdr_tready (m_rfnoc_chdr_tready),
    .s_rfnoc_ctrl_tdata  (m_ctrl.tdata),
    .s_rfnoc_ctrl_tlast  (m_ctrl.tlast),
    .s_rfnoc_ctrl_tvalid (m_ctrl.tvalid),
    .s_rfnoc_ctrl_tready (m_ctrl.tready),
    .m_rfnoc_ctrl_tdata  (s_ctrl.tdata),
    .m_rfnoc_ctrl_tlast  (s_ctrl.tlast),
    .m_rfnoc_ctrl_tvalid (s_ctrl.tvalid),
    .m_rfnoc_ctrl_tready (s_ctrl.tready)
  );

  //---------------------------------------------------------------------------
  // Clock Sanity Check
  //---------------------------------------------------------------------------

  // Count edges on each framework clock over a 1 us window. Expected over 1 us:
  // chdr (5 ns) -> 200, ctrl (25 ns) -> 40, ce (3.75 ns) -> ~266 (ce_clk feeds
  // the block's MMCM, which derives the 250 MHz data-path clock). A zero would
  // mean that clock is dead.
  initial begin : clk_check
    int n_chdr, n_ctrl, n_ce;
    n_chdr = 0; n_ctrl = 0; n_ce = 0;
    fork
      forever @(posedge rfnoc_chdr_clk) n_chdr++;
      forever @(posedge rfnoc_ctrl_clk) n_ctrl++;
      forever @(posedge ce_clk)         n_ce++;
    join_none
    #1000;
    $display("CLOCKCHK @1us: chdr=%0d (exp ~200) ctrl=%0d (exp ~40) ce=%0d (exp ~266)",
             n_chdr, n_ctrl, n_ce);
    `ASSERT_ERROR(n_chdr > 0 && n_ctrl > 0 && n_ce > 0,
                  "One or more framework clocks is not toggling");
  end

  //---------------------------------------------------------------------------
  // Helper Tasks
  //---------------------------------------------------------------------------

  // Read the txPayloadReady status register (bit 0).
  task automatic get_tx_payload_ready(output bit ready);
    logic [31:0] val;
    blk_ctrl.reg_read(REG_TX_PAYLOAD_READY_ADDR, val);
    ready = val[0];
  endtask : get_tx_payload_ready

  // Feed "nsamps" int8 payload samples into the txPayload input port. The
  // samples are packed one-per-byte into CHDR words (least-significant byte
  // first, so sample N maps to txPayload sample N) and streamed as a series of
  // CHDR packets sized by the master data port's PKT_SIZE_BYTES.
  //
  // Sending is throttled purely by CHDR flow control: the input FIFO drives
  // txPayloadReady, which is the tready of the txPayload AXI-Stream. When the
  // FIFO fills (occupancy >= 40812) txPayloadReady deasserts, tready drops, and
  // the BFM automatically stops sending; once the block is enabled and the FIFO
  // drains, txPayloadReady reasserts and sending resumes. This is the hardware
  // realization of "stop sending when txPayloadReady is false, resume when it
  // becomes true."
  task automatic feed_payload(input byte unsigned samples[], input int nsamps);
    chdr_word_t words[$];
    int         word_idx;
    int         byte_lane;

    // Pack every sample into a flat CHDR-word queue.
    words = {};
    for (int b = 0; b < nsamps; b++) begin
      word_idx  = b / ($bits(chdr_word_t)/8);
      byte_lane = b % ($bits(chdr_word_t)/8);
      if (byte_lane == 0) words.push_back('0);
      words[word_idx][byte_lane*8 +: 8] = samples[b];
    end

    // send_packets splits the data into max-payload-sized packets (set by
    // connect_master_data_port) and honors flow control between them.
    blk_ctrl.send_packets(0, words, nsamps);
  endtask : feed_payload

  // Receive "nsamps" sc16 output samples from the txData output port and compare
  // them against the MATLAB golden reference (expected_mem). Each 32-bit item is
  // {I[31:16], Q[15:0]}. Every I and Q value must be within TOL_LSB of the
  // expected value. Reports peak magnitude, the worst-case deviation, and the
  // number of out-of-tolerance samples, then asserts that none failed.
  //
  // Note: extract the 16-bit I/Q via explicit 16-bit regs + $signed(). A
  // signed'() cast applied directly to a part-select (signed'(rx[i][31:16]))
  // does not sign/zero-extend correctly under xsim and yields garbage.
  //
  // skip_lead: number of leading output samples to receive and count toward
  // nsamps but exclude from the golden tolerance check. Used for a frame that
  // follows another (no flush in between), whose first WINDOW_OVERLAP samples
  // legitimately carry the previous frame's windowing overlap-add tail.
  task automatic capture_output(input int nsamps, input int skip_lead = 0);
    item_t rx[$];
    int    got;
    int    peak;
    int    i_samp;
    int    q_samp;
    int    exp_i;
    int    exp_q;
    int    dev_i;
    int    dev_q;
    int    max_dev;
    int    nfail;
    int    mag;
    logic [15:0] hi16;
    logic [15:0] lo16;

    got     = 0;
    peak    = 0;
    max_dev = 0;
    nfail   = 0;
    while (got < nsamps) begin
      blk_ctrl.recv_items(0, rx);
      foreach (rx[i]) begin
        if (got >= nsamps) break;
        hi16   = rx[i][31:16];
        lo16   = rx[i][15:0];
        i_samp = $signed(hi16);
        q_samp = $signed(lo16);

        // Deviation from the golden reference. Skip the leading overlap samples
        // (skip_lead) for a frame that follows another: their windowing
        // overlap-add tail is correct continuous-TX behavior, not an error.
        if (got >= skip_lead) begin
          hi16  = expected_mem[got][31:16];
          lo16  = expected_mem[got][15:0];
          exp_i = $signed(hi16);
          exp_q = $signed(lo16);
          dev_i = i_samp - exp_i; if (dev_i < 0) dev_i = -dev_i;
          dev_q = q_samp - exp_q; if (dev_q < 0) dev_q = -dev_q;
          if (dev_i > max_dev) max_dev = dev_i;
          if (dev_q > max_dev) max_dev = dev_q;
          if (dev_i > TOL_LSB || dev_q > TOL_LSB) begin
            if (nfail < 20) begin
              $display("  MISMATCH @%0d: got (%0d, %0d) exp (%0d, %0d) dev (%0d, %0d)",
                       got, i_samp, q_samp, exp_i, exp_q, dev_i, dev_q);
            end
            nfail++;
          end
        end

        // Peak magnitude (sanity stat).
        mag = (i_samp < 0) ? -i_samp : i_samp;
        if (((q_samp < 0) ? -q_samp : q_samp) > mag) mag = (q_samp < 0) ? -q_samp : q_samp;
        if (mag > peak) peak = mag;
        got++;
      end
    end
    $display("capture_output: %0d sc16 samples, peak |IQ| = %0d", got, peak);
    $display("capture_output: max deviation = %0d LSB (tol %0d), %0d samples out of tolerance",
             max_dev, TOL_LSB, nfail);
    `ASSERT_ERROR(nfail == 0,
                  "Captured OFDM output deviates from MATLAB reference beyond tolerance");
  endtask : capture_output

  // Drain the block output through the continuous 250 MHz radio sink and
  // compare what the radio "transmits" against the golden reference, mirroring
  // transceiver.py: strip the leading underflow run, then verify the captured
  // samples in order. Mid-stream underflows mean the block starved the radio
  // (the hardware failure); they corrupt the capture and fail the test.
  task automatic capture_output_radio(input int nsamps);
    item_t  rx[$];
    int     pushed;
    int     vi;
    int     got;
    int     nfail;
    int     max_dev;
    int     dev_i;
    int     dev_q;
    int     exp_i;
    int     exp_q;
    int     i_samp;
    int     q_samp;
    logic [15:0] hi16;
    logic [15:0] lo16;
    logic [31:0] w;

    // Producer: pull packets as fast as the block emits them (BFM never the
    // bottleneck) and hand them to the radio FIFO; block when the FIFO is full
    // so the 250 MHz radio rate -- not the BFM -- backpressures the block.
    blk_ctrl.set_slave_stall_prob(0, 0);
    pushed = 0;
    while (pushed < nsamps) begin
      blk_ctrl.recv_items(0, rx);
      foreach (rx[i]) begin
        while (radio_fifo.size() >= RADIO_TX_FIFO_DEPTH) @(posedge radio_clk);
        radio_fifo.push_back(rx[i]);
        pushed++;
        // Prime the radio TX FIFO half full before it starts transmitting, like
        // a real TX that buffers before going on air.
        if (!radio_run && radio_fifo.size() >= RADIO_TX_FIFO_DEPTH/2)
          radio_run = 1'b1;
      end
    end
    // Let the radio drain what is left, then stop it so the trailing empty
    // cycles are not miscounted as underflows.
    while (radio_fifo.size() > 0) @(posedge radio_clk);
    radio_run = 1'b0;

    // Align to golden exactly like transceiver.py: the on-air stream begins with
    // a short startup transient (pipeline fill before the first golden sample),
    // so find where golden sample 0 lands by matching the first few samples,
    // then compare the contiguous overlap that follows.
    vi = -1;
    for (int off = 0; off + 8 < radio_cap.size() && off < 4096; off++) begin
      bit hit = 1'b1;
      for (int k = 0; k < 8; k++) begin
        w   = radio_cap[off+k];
        i_samp = $signed(w[31:16]); q_samp = $signed(w[15:0]);
        exp_i  = $signed(expected_mem[k][31:16]); exp_q = $signed(expected_mem[k][15:0]);
        dev_i = i_samp - exp_i; if (dev_i < 0) dev_i = -dev_i;
        dev_q = q_samp - exp_q; if (dev_q < 0) dev_q = -dev_q;
        if (dev_i > TOL_LSB || dev_q > TOL_LSB) begin hit = 1'b0; break; end
      end
      if (hit) begin vi = off; break; end
    end
    `ASSERT_ERROR(vi >= 0, "Could not align continuous-radio capture to golden");
    $display("capture_output_radio: aligned to golden at capture offset %0d", vi);

    got = 0; nfail = 0; max_dev = 0;
    while (got < nsamps && vi < radio_cap.size()) begin
      w      = radio_cap[vi]; vi++;
      hi16   = w[31:16];  lo16 = w[15:0];
      i_samp = $signed(hi16); q_samp = $signed(lo16);
      hi16   = expected_mem[got][31:16]; lo16 = expected_mem[got][15:0];
      exp_i  = $signed(hi16); exp_q = $signed(lo16);
      dev_i  = i_samp - exp_i; if (dev_i < 0) dev_i = -dev_i;
      dev_q  = q_samp - exp_q; if (dev_q < 0) dev_q = -dev_q;
      if (dev_i > max_dev) max_dev = dev_i;
      if (dev_q > max_dev) max_dev = dev_q;
      if (dev_i > TOL_LSB || dev_q > TOL_LSB) begin
        if (nfail < 20)
          $display("  RADIO MISMATCH @%0d: got (%0d, %0d) exp (%0d, %0d)",
                   got, i_samp, q_samp, exp_i, exp_q);
        nfail++;
      end
      got++;
    end

    $display("capture_output_radio: compared %0d samples, %0d mid-stream radio underflow cycles",
             got, radio_underflows_mid);
    $display("capture_output_radio: max deviation = %0d LSB (tol %0d), %0d/%0d out of tolerance",
             max_dev, TOL_LSB, nfail, got);
    `ASSERT_ERROR(radio_underflows_mid == 0,
                  "Radio underflowed mid-stream: block cannot sustain the 250 MHz radio rate");
    `ASSERT_ERROR(nfail == 0,
                  "Continuous-radio capture deviates from MATLAB reference beyond tolerance");
  endtask : capture_output_radio

  // True for a payload-bearing data symbol (0-based index). Symbol 1 is sync and
  // every 4th symbol from 2 (refOFDMIdx = 2,6,10,...) is a reference symbol; both
  // are payload-independent and carry no payload bits.
  function automatic bit symbol_is_data(input int s0);
    int s1;
    s1 = s0 + 1;
    if (s1 == 1)              return 1'b0;
    if (((s1 - 2) % 4) == 0)  return 1'b0;
    return 1'b1;
  endfunction : symbol_is_data

  // Capture nsamps sc16 output items into demod_cap. Uses the compacted recv path
  // (reads only on tvalid && tready), which is aligned to golden output sample 0
  // exactly like capture_output. Output backpressure is governed by the caller's
  // slave stall probability, so a parallel "adversary" can hammer tready to
  // stress the core's clk_enable freeze/resume path while this collects.
  task automatic capture_collect(input int nsamps);
    item_t rx[$];
    int    got;
    demod_cap = {};
    got = 0;
    while (got < nsamps) begin
      blk_ctrl.recv_items(0, rx);
      foreach (rx[i]) begin
        if (got >= nsamps) break;
        demod_cap.push_back(rx[i]);
        got++;
      end
    end
  endtask : capture_collect

  // Demodulate the first n_data_syms_check data symbols of demod_cap and assert
  // every recovered QPSK bit-pair equals the fed payload byte. align0 is the
  // demod_cap index of golden output sample 0 (0 for the compacted recv path).
  task automatic demod_check_payload(input int align0,
                                     input byte unsigned fed[],
                                     input int n_data_syms_check);
    real xr [FFT_LEN_C];
    real xi [FFT_LEN_C];
    real re_acc;
    real im_acc;
    int  pp;
    int  dsyms;
    int  nfail;
    int  total;
    int  base;
    int  k;
    int  m;
    logic [31:0] w;
    logic [15:0] hi16;
    logic [15:0] lo16;
    byte unsigned rec;
    byte unsigned exp_b;

    pp = 0; dsyms = 0; nfail = 0; total = 0;
    for (int s = 0; s < 240; s++) begin
      if (!symbol_is_data(s)) continue;
      if (dsyms >= n_data_syms_check) begin pp += N_DATA_BINS; continue; end

      // CP-stripped FFT window for this symbol. Use explicit 16-bit regs +
      // $signed() (a $signed cast on a part-select yields garbage under xsim).
      base = align0 + s*SPS_C + CP_LEN_C;
      for (int n = 0; n < FFT_LEN_C; n++) begin
        w     = demod_cap[base + n];
        hi16  = w[31:16];  lo16 = w[15:0];
        xr[n] = $itor($signed(hi16));   // I (upper sc16 half)
        xi[n] = $itor($signed(lo16));   // Q (lower sc16 half)
      end

      // 256-pt DFT at each data bin (e^-j convention, matching numpy fft), QPSK
      // demap, compare to the fed payload byte for this subcarrier.
      for (int i = 0; i < N_DATA_BINS; i++) begin
        k = demod_bins[i];
        re_acc = 0.0; im_acc = 0.0;
        for (int n = 0; n < FFT_LEN_C; n++) begin
          m = (k*n) % FFT_LEN_C;
          re_acc += xr[n]*demod_cos[m] + xi[n]*demod_sin[m];
          im_acc += xi[n]*demod_cos[m] - xr[n]*demod_sin[m];
        end
        rec   = ((re_acc < 0.0) ? 8'd1 : 8'd0) | ((im_acc < 0.0) ? 8'd2 : 8'd0);
        exp_b = fed[pp + i] & 8'h3;
        total++;
        if (rec !== exp_b) begin
          if (nfail < 20)
            $display("  DEMOD MISMATCH data-sym %0d bin %0d: rec=%0d exp=%0d (re=%.0f im=%.0f)",
                     s+1, k, rec, exp_b, re_acc, im_acc);
          nfail++;
        end
      end
      pp += N_DATA_BINS;
      dsyms++;
    end
    $display("demod_check_payload: %0d data symbols, %0d/%0d subcarrier bit-pairs mismatched",
             dsyms, nfail, total);
    `ASSERT_ERROR(nfail == 0,
        "Demodulated subcarrier bits do not match the fed payload (payload-delivery bug)");
  endtask : demod_check_payload

