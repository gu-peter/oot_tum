    test.start_test("Verify Block Info", 2us);
    `ASSERT_ERROR(blk_ctrl.get_noc_id() == NOC_ID, "Incorrect NOC_ID Value");
    `ASSERT_ERROR(blk_ctrl.get_num_data_i() == NUM_PORTS_I, "Incorrect NUM_DATA_I Value");
    `ASSERT_ERROR(blk_ctrl.get_num_data_o() == NUM_PORTS_O, "Incorrect NUM_DATA_O Value");
    `ASSERT_ERROR(blk_ctrl.get_mtu() == MTU, "Incorrect MTU Value");
    test.end_test();

    //--------------------------------
    // Test Sequences
    //--------------------------------

    begin : test_feed_enable_capture
      byte unsigned tx_samples[];
      logic [31:0]  enable_rb;
      bit           ready_rb;

      test.start_test("Feed payload, fill FIFO, enable, capture OFDM output", 50ms);

      // Use the MATLAB reference payload (loaded into input_mem). Only the two
      // LSBs are used by the block (QPSK I/Q bits); the file already encodes
      // bit0 -> txPayload_0, bit1 -> txPayload_1.
      tx_samples = new[NUM_TX_SAMPS];
      foreach (tx_samples[i]) begin
        tx_samples[i] = input_mem[i];
      end

      // Make sure the block starts disabled so the input FIFO can fill without
      // being drained (pops require enable). No output is produced until we
      // enable the transmitter.
      blk_ctrl.reg_write(REG_ENABLE_ADDR, 0);

      // Feed the payload and assert "enable" once the FIFO has filled.
      //
      //  - The producer streams all NUM_TX_SAMPS samples. Once the input FIFO
      //    fills (occupancy >= 40812) txPayloadReady deasserts and the BFM
      //    stalls automatically; the remaining samples flush only after enable
      //    drains the FIFO and txPayloadReady reasserts.
      //  - The enabler waits for the FIFO-full condition (txPayloadReady low),
      //    holds off 50 ce_clk cycles, then writes the enable register so the
      //    block starts modulating and emitting output samples.
      fork
        begin : producer
          feed_payload(tx_samples, NUM_TX_SAMPS);
        end
        begin : enabler
          // Ensure we start from the ready (FIFO not full) state, then wait for
          // the block to report the FIFO is full.
          wait (dut.m_txPayload_axis_tready === 1'b1);
          wait (dut.m_txPayload_axis_tready === 1'b0);
          $display("Input FIFO full (txPayloadReady low); enabling in 50 cycles");
          repeat (50) @(posedge ce_clk);
          blk_ctrl.reg_write(REG_ENABLE_ADDR, 1);
        end
      join

      // Confirm the enable register took effect.
      blk_ctrl.reg_read(REG_ENABLE_ADDR, enable_rb);
      `ASSERT_ERROR(enable_rb[0] == 1'b1, "enable register did not read back as set");

      // Sustained-backpressure regression. The OFDM_Transmitter core has no
      // output-ready port; it can only be stalled via clk_enable. On hardware
      // the radio applies *sustained* (not random) backpressure at 250 MHz while
      // the core runs at ce_clk (266.667 MHz), so a small output FIFO fills and
      // -- if clk_enable is not gated by s_txData_axis_tready -- every overflow
      // silently drops a sample, corrupting the frame from ~symbol 2 onward.
      // The default BFM only stalls randomly (STALL_PROB), which averages out
      // and never sustains "full", so it misses this. Apply the stall here,
      // right after enable, while the core is starting to stream output and the
      // output FIFO is the only buffer: hold the slave tready low long enough to
      // overflow any reasonable FIFO, then resume. With proper backpressure
      // (clk_enable = s_txData_axis_tready) the core just freezes and no samples
      // are lost, so the capture below still matches golden bit-for-bit; with the
      // bug, the dropped samples make capture_output fail mid-frame.
      blk_ctrl.set_slave_stall_prob(0, 100);  // never assert tready: sustained full
      repeat (4096) @(posedge ce_clk);        // >> any plausible output FIFO depth
      blk_ctrl.set_slave_stall_prob(0, STALL_PROB);

      // Completion of feed_payload above already proves the stall resumed (the
      // last samples could only be accepted after enable drained the FIFO). As
      // a final check, wait for txPayloadReady to settle back to true now that
      // all input has been consumed and no more is being pushed.
      do begin
        get_tx_payload_ready(ready_rb);
      end while (!ready_rb);

      // Capture the modulated OFDM output and compare against the reference.
      capture_output(NUM_RX_SAMPS);

      test.end_test();
    end : test_feed_enable_capture

    begin : test_continuous_radio_sink
      byte unsigned tx_samples[];
      bit           ready_rb;

      test.start_test("Feed, enable, capture through continuous 250 MHz radio sink", 80ms);

      tx_samples = new[NUM_TX_SAMPS];
      foreach (tx_samples[i]) tx_samples[i] = input_mem[i];

      // Start from a clean slate: flush any residual frame-1 samples out of the
      // block and BFM so this capture aligns to golden sample 0.
      blk_ctrl.reg_write(REG_ENABLE_ADDR, 0);
      blk_ctrl.flush_and_reset();

      // Reset the radio-sink model state for this run.
      radio_fifo            = {};
      radio_cap             = {};
      radio_run             = 1'b0;
      radio_underflows_mid  = 0;

      fork
        begin : producer2
          feed_payload(tx_samples, NUM_TX_SAMPS);
        end
        begin : enabler2
          wait (dut.m_txPayload_axis_tready === 1'b1);
          wait (dut.m_txPayload_axis_tready === 1'b0);
          repeat (50) @(posedge ce_clk);
          blk_ctrl.reg_write(REG_ENABLE_ADDR, 1);
        end
        begin : sink2
          // Capture the whole frame through the continuous radio sink.
          capture_output_radio(NUM_RX_SAMPS);
        end
      join

      do begin
        get_tx_payload_ready(ready_rb);
      end while (!ready_rb);

      test.end_test();
    end : test_continuous_radio_sink

    begin : test_frame_gating_recovery
      byte unsigned tx_samples[];
      longint       gap_valid_cnt;

      test.start_test("Frame gating: two frames across a payload gap, both golden", 80ms);

      tx_samples = new[NUM_TX_SAMPS];
      foreach (tx_samples[i]) tx_samples[i] = input_mem[i];

      // Clean slate, then enable. The frame-gating FSM holds the core (its
      // 'enable' input, run_frame) low until a full frame of payload is buffered,
      // so enabling before any payload is present is safe -- no output is emitted
      // until a whole frame is queued.
      blk_ctrl.reg_write(REG_ENABLE_ADDR, 0);
      blk_ctrl.flush_and_reset();
      blk_ctrl.reg_write(REG_ENABLE_ADDR, 1);

      // ---- Frame 1 ----
      feed_payload(tx_samples, NUM_TX_SAMPS);
      capture_output(NUM_RX_SAMPS);

      // ---- Deliberate payload gap ----
      // Frame 1 drained the input FIFO, so the gated core must pause at the frame
      // boundary and emit nothing. This is exactly the underrun the gating exists
      // to handle: a free-running core would here start a second frame against an
      // empty FIFO and modulate garbage (the hardware failure). Settle a few
      // cycles to let the final frame-1 sample retire, then assert the core is
      // quiescent for a long window.
      repeat (200) @(posedge dut.axis_data_clk);
      `ASSERT_ERROR(dut.run_frame === 1'b0,
                    "Frame gating: run_frame should be low while no frame is buffered");
      gap_valid_cnt = 0;
      repeat (4000) begin
        @(posedge dut.axis_data_clk);
        if (dut.s_txData_axis_tvalid) gap_valid_cnt++;
      end
      `ASSERT_ERROR(gap_valid_cnt == 0,
                    "Frame gating: core produced output during the payload gap (would be garbage)");

      // ---- Frame 2 ----
      // Feed the same golden payload again: the FSM must resume from a clean
      // frame boundary so frame 2 matches golden, proving the payload and the
      // framer stayed aligned across the gap. Frame 2 follows frame 1 without an
      // intervening flush, so its first WINDOW_OVERLAP samples legitimately
      // carry frame 1's windowing overlap-add tail (correct continuous-TX
      // behavior, verified by probe: the deviation is exactly WINDOW_OVERLAP
      // samples long and decays as the window ramp-down tapers to zero). Skip
      // those in the golden comparison; every later sample must still match.
      feed_payload(tx_samples, NUM_TX_SAMPS);
      capture_output(NUM_RX_SAMPS, WINDOW_OVERLAP);

      test.end_test();
    end : test_frame_gating_recovery

    begin : test_arbitrary_payload_backpressure
      byte unsigned arb[];
      logic [15:0]  lfsr;
      bit           ready_rb;

      test.start_test("Arbitrary payload + adversarial backpressure: demod must match fed bits", 80ms);

      // Build an arbitrary, content-dependent payload (16-bit Galois LFSR, 2
      // payload bits per byte). Unlike the constant/ramp loopback probes, an
      // arbitrary payload is sensitive to any dropped, duplicated, or mis-timed
      // payload read, so the demod check below catches payload-delivery
      // corruption that the golden (and const/ramp) tests can miss -- the
      // hardware failure where data subcarriers carry random bits.
      arb  = new[NUM_TX_SAMPS];
      lfsr = 16'hACE1;
      foreach (arb[i]) begin
        arb[i] = lfsr[1:0];
        lfsr   = lfsr[0] ? ((lfsr >> 1) ^ 16'hB400) : (lfsr >> 1);
      end

      // Clean slate so this frame aligns to golden output sample 0 (sync symbol).
      blk_ctrl.reg_write(REG_ENABLE_ADDR, 0);
      blk_ctrl.flush_and_reset();

      fork
        begin : producer4
          feed_payload(arb, NUM_TX_SAMPS);
        end
        begin : enabler4
          wait (dut.m_txPayload_axis_tready === 1'b1);
          wait (dut.m_txPayload_axis_tready === 1'b0);
          repeat (50) @(posedge ce_clk);
          blk_ctrl.reg_write(REG_ENABLE_ADDR, 1);
        end
        begin : adversary4
          // Hammer output backpressure in long sustained bursts while the core
          // modulates. Sustained "full" keeps the output FIFO full so the core's
          // clk_enable (= s_txData_axis_tready) toggles hard, repeatedly freezing
          // and resuming the core while payload streams in -- the condition under
          // which a payload-read flow-control bug drops/duplicates samples. The
          // default random STALL_PROB averages out and never sustains "full", so
          // it does not exercise this; the one-shot stall in test 1 freezes only
          // once. Here we cycle freeze/resume repeatedly across the frame start.
          repeat (8) begin
            blk_ctrl.set_slave_stall_prob(0, 100);   // never ready: sustained full
            repeat (3000) @(posedge ce_clk);
            blk_ctrl.set_slave_stall_prob(0, STALL_PROB);
            repeat (1000) @(posedge ce_clk);
          end
          blk_ctrl.set_slave_stall_prob(0, STALL_PROB);
        end
        begin : sink4
          capture_collect(NUM_RX_SAMPS);
        end
      join

      do begin
        get_tx_payload_ready(ready_rb);
      end while (!ready_rb);

      // Compacted recv path is aligned to golden output sample 0, so align0 = 0.
      demod_check_payload(0, arb, DEMOD_SYMS_TO_CHECK);

      test.end_test();
    end : test_arbitrary_payload_backpressure

